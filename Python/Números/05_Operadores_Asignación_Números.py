print(f"""
Operador de asignación, para operar con la variable y se pone el simbolo antes del =
Las operaciones se realizan con el resultado obtenido de la operación anterior
-----""")
total = 100
print(f"""total = 100 -> {total}""")
total = total + 10
print(f"""total = total + 10 -> {total}""")
total += 10
print(f"""total += 10 -> {total}""")
total -= 10
print(f"""total -= 10 -> {total}""")
total *= 2
print(f"""total *= 2 -> {total}""")
total /= 10
print(f"""total /= 10 -> {total}""")
total //= 10
print(f"""total //= 10 -> {total}""")
total **= 2
print(f"""total **= 2 -> {total}""")
total %= 2
print(f"""total %= 2 -> {total}""")
print('-----')
product_two = 120
product_three = 10
total += product_two
total += product_three
print(f"""product_two = 120
product_three = 10
total += product_two
total += product_three
print(total) -> {total}""")
